function stylize(text = '', style = 'fancy') {
  const map = maps[style] || maps.fancy;
  return String(text).split('').map((ch) => {
    const lower = ch.toLowerCase();
    if (!map[lower]) return ch;
    return ch === lower ? map[lower] : map[lower];
  }).join('');
}

